// planExport.ts — Project + height-resolved objects → a pure ExportPlan (design §3.4).
//
// No I/O: it returns { folderName, files[], warnings } describing exactly what the installer
// (main process, M1b) should write into `scenery/poi/<folderName>/`. Keeping this pure makes
// the whole POI package golden-testable byte-for-byte, the way the Race App exporter was.

import type { ExportPlan, PoiFile, Project, ResolvedObject } from "../project/types";
import { centroid, poiFolderName } from "../geo/poiName";
import { shiftEastNorth } from "../geo/geo";
import { buildToc } from "./tocWriter";
import { buildTsl } from "./tslWriter";
import { computeAnchor, ANCHOR_ASSETS } from "./plantAnchor";

// A POI's two payload files are named `poi.tsl` / `poi.toc` (format bible), and the .tsl
// references the .toc by this basename.
const POI_BASENAME = "poi";

/** Marker line dropped in README.txt so the installer can tell PCT-made POIs apart from
 *  hand-made or third-party ones when listing/uninstalling (design §3.4 `byPct`). */
export const POI_README_MARKER = "Generated by PCT (POI Creation Tool)";

function buildReadme(project: Project, resolved: ResolvedObject[]): string {
  return [
    project.name,
    POI_README_MARKER,
    "",
    `Objects: ${resolved.length}`,
    "",
    "Install: copy this whole folder (keep its name) into your Aerofly FS 4 user",
    "folder's scenery/poi/ , then restart Aerofly FS 4 and fly to the area:",
    "  Windows: Documents\\Aerofly FS 4\\scenery\\poi\\",
    "  macOS:   ~/Library/Application Support/Aerofly FS 4/scenery/poi/",
    "",
  ].join("\n");
}

/** Plan the POI package. `project.reference` sets the folder-name anchor; when null it falls
 *  back to the centroid of the placed objects (design §2.2). */
/** Apply the project's global export shift (forum #12) to every object's position: nudge each one
 *  `shift` metres east/north. The height was resolved at the object's REAL location, so shifting the
 *  horizontal coordinate afterwards is correct — it only lines the objects up with FS4's satellite
 *  tiles. A zero/absent shift returns the same array (no-op), keeping the golden output stable. */
function applyShift(resolved: ResolvedObject[], shift: Project["shift"]): ResolvedObject[] {
  if (!shift || (shift.east === 0 && shift.north === 0)) return resolved;
  return resolved.map((o) => ({ ...o, position: shiftEastNorth(o.position, shift.east, shift.north) }));
}

export function planExport(project: Project, resolved: ResolvedObject[]): ExportPlan {
  const warnings: string[] = [];
  if (resolved.length === 0) {
    warnings.push("The POI has no objects — nothing will appear in the sim.");
  }

  const objects = applyShift(resolved, project.shift);
  const ref = project.reference ?? centroid(objects.map((o) => o.position));
  const folderName = poiFolderName(ref, project.poiName);
  const tocFileName = objects.length > 0 ? POI_BASENAME : null;
  // A POI with plants also gets the reference-POI anchor (plantAnchor.ts): the anchor object emitted in
  // the .tsl plus its bundled mesh/texture assets. Computed from the SHIFTED objects so it lines up with
  // them; null (no anchor, no assets) for any POI without plants.
  const anchor = computeAnchor(objects);

  const files: PoiFile[] = [
    { relPath: `${POI_BASENAME}.tsl`, content: buildTsl({ tocFileName, anchor }) },
    { relPath: `${POI_BASENAME}.toc`, content: buildToc(objects) },
    { relPath: "README.txt", content: buildReadme(project, objects) },
  ];

  return { folderName, files, assets: anchor ? [...ANCHOR_ASSETS] : [], warnings };
}
